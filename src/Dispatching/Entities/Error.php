declare(strict_types=1);

namespace Appwilio\RussianPostSDK\Dispatching\Entities;

final class Error
{
    /** @var string */
    private $code;

    /** @var string|null */
    private $message;

    public function __construct(string $code, ?string $message = null)
    {
        $this->code = $code;
        $this->message = $message;
    }

    public function getCode(): string
    {
        return $this->code;
    }

    public function getMessage(): string
    {
        return $this->message;
    }
}
